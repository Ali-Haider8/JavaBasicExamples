package LectureOne;

public class print_1_to_100 {
    public static void main(String[] args) {
        int i;

        System.out.println("Print 1-100 app.");
        System.out.println("---------------");

        for (i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        System.out.println("---------------");
    }
}
