package Lecture4;

public class escape_characters {

    public static void main(String[] args) {
        /*
         * \t
         * \n
         * \'
         * \"
         * \r
         * */
        System.out.println("Ali\t18");
        System.out.println("Name:\nAge:");
        System.out.println("He said \"Hello\" ");
        System.out.println("It\'s easy");
        System.out.println("It's easy");
        System.out.println("c:\\users\\ali\\java");
        System.out.println("Hello\rWorld");
    }
}
